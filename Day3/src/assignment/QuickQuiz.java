package assignment;

public class QuickQuiz {
    public static void main(String[] args) {
        for(int i=100;i<=200;i++ ){
            System.out.println(i);
        }
        int i = 100;
        while(i<=200){
            System.out.println(i);
            i++;
        }
        i=100;
        do{
            System.out.println(i);
            i++;
        }while(i<=200);
    }
}
